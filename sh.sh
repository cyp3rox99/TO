#!/bin/sh

# رمز الجمجمة باللون الأحمر (ANSI escape code)
RED='\033[31m'
RESET='\033[0m'

# شعار الجمجمة (يمكن تغييره حسب الرغبة)
SKULL="
${RED}      ______
     /      \\
    |  () () |
    |   __   |
     \\______/ ${RESET}
"

# تنظيف الشاشة
clear

# عرض شعار الجمجمة الأحمر
echo "$SKULL"

# تعيين موجه الأوامر ليكون فقط $
export PS1='\$ '